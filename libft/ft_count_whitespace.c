#include "libft.h"

int	ft_count_whitespace(char const *s)
{
	int i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	return (i);
}
